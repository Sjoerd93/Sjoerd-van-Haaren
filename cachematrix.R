## The cachematrix.R file contains two functions, makeCacheMatrix and cacheSolve(). 
## The first function creates an R object that stores a matrix and its inverse.

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinverse <- function(inverse) m <<- inverse
    getinverse <- function() m
    list(set = set, get = get,
         setinverse = setinverse,
         getinverse = getinverse)
}


## The second function requires an argument that is returned by MakeCacheMatrix() in order to retrieve the inverse from the cached value that is stored in the MakeCacheMatrix() object's environment.


cacheSolve <- function(x, ...) {
    m <- x$getinverse()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setinverse(m)
    m
}
